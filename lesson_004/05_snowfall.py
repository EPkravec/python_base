while True:
    sd.clear_screen()
    for idx, val in enumerate(x):
        point = sd.get_point(x[idx], y[idx])
        y[idx] -= random.randint(1, 15)
        if y[idx] < 0:
            y[idx] = 600
        x[idx] += random.randint(-12, 25)
        x[idx] -= random.randint(-12, 25)
